        <p class="post-meta">
            Para la región de la Cuenca del Papaloapan, 
            la creación del Instituto Tecnológico le ha significado un gran
            logro en su haber, ya que ha sentado las bases que esquematizaran 
            el desarrollo futuro de la región y es la oportunidad de muchos 
            jóvenes de alcanzar estudios superiores. Fue un 7 de Junio de 1997
            día memorable en que se conmemora a la libertad de expresión, cuando
            el Gobernador Constitucional del Estado de Veracruz hizo público 
            el establecimiento del INSTITUTO TECNOLÓGICO SUPERIOR DE 
            COSAMALOAPAN a los medios de comunicación
        </p>
        <p class="post-meta">
            La primera convocatoria para los Jóvenes que aspiraran cursar sus
            estudios en el Instituto Tecnológico Superior de Cosamaloapan se 
            publicó en el diario el Dictamen con fecha de 8 de Agosto de 1997. 
            También se comunica la convocatoria por medio de las estaciones de
            radio XEQO y XEFU a partir del sábado 9 de Agosto, el domingo 10 
            de Agosto se asistió al Programa Libre Expresión de la Estación 
            de radio para comentar el contenido de la Convocatoria. A partir 
            del 11 de Agosto de 1997 se inicia el proceso de expedición de 
            fichas para el examen de admisión. Se recibieron 213 solicitudes de
            nuevo ingreso, de los cuales se inscriben 193 alumnos de nuevo 
            ingreso, 100 en Licenciatura en Informática y 93 en Ingeniería 
            Industrial.
        </p>
